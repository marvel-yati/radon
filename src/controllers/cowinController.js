let getTemperature = async function (req, res) {
    try {
        let cities = ["Bengaluru", "Mumbai", "Delhi", "Kolkata", "Chennai", "London", "Moscow"]
        //let districtid = req.query.districtId
        //let date = req.query.date
        let cityTemps = []
        for(let city of cities){
            let options = {
                method: "get",
                url: `http://api.openweathermap.org/data/2.5/weather?q=${city}&appid=d528a6d1c0fd73798ba3310a081a780f`
            }
        let result = await axios(options);
        cityTemps.push({City: city, Temperature : result.data.main.temp})
        }
        cityTemps = cityTemps.sort(function(a,b){return a.Temperature - b.Temperature})
        
        res.status(200).send({ msg: cityTemps, status: true })
    }
    catch (err) {
        console.log(err)
        res.status(500).send({ msg: err.message })
    }
}

